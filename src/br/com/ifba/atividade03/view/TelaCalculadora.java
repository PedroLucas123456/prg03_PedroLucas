    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Captura os valores digitados pelo usuário nos campos de texto
        int diaNascimento = Integer.parseInt(campoDia.getText());  
        int mesNascimento = Integer.parseInt(campoMes.getText());
        int anoNascimento = Integer.parseInt(campoAno.getText());

        // Obtém a data atual do sistema
        java.time.LocalDate dataAtual = java.time.LocalDate.now();
        int diaAtual = dataAtual.getDayOfMonth();
        int mesAtual = dataAtual.getMonthValue();
        int anoAtual = dataAtual.getYear();

        // Calcula a idade de forma básica (ano atual - ano de nascimento)
        int idade = anoAtual - anoNascimento;

        // Ajusta a idade caso o aniversário ainda não tenha ocorrido no ano atual
        if (mesAtual < mesNascimento || (mesAtual == mesNascimento && diaAtual < diaNascimento)){
            idade--;
        }
        // Mostra o resultado no campo de saída
        campoResultado.setText("Idade: " + idade + " Anos");
    }//GEN-LAST:event_btnCalcularActionPerformed

